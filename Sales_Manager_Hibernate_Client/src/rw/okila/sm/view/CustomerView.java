    public String EMAIL_PATTERN = "^(.+)@(\\S+)$";
    public Pattern pattern = Pattern.compile(EMAIL_PATTERN);

    public boolean emailIsValid(String email) {
        Matcher matcher = pattern.matcher(email);

        return matcher.matches();
    }

