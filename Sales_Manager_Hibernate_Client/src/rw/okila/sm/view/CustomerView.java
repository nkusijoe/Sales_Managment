    public void saveCust() {
        try {
 
            String fname = fnameTxt.getText();
            String lname = lnameTxt.getText();
            String phone = phoneTxt.getText();
            String email = emailTxt.getText();
            String tin = tinTxt.getText();
            String address =addressTxt.getText();
            String type =typeCombo.getSelectedItem().toString();
            Customers customer = new Customers(tin, fname, lname, phone, email, address, type);

            Registry registry = LocateRegistry.getRegistry("localhost", 9876);
            Object obj = registry.lookup("customerService");
            CustomerServiceInterface custService = (CustomerServiceInterface) obj;
            custService.saveCustomer(customer);
                    } catch (RemoteException ex) {
            JOptionPane.showMessageDialog(this, "Error. Customer not saved.");
        } catch (NotBoundException ex) {
            Logger.getLogger(CustomerView.class.getName()).log(Level.SEVERE, null, ex);
        }

            //UPDATE FORM
            fnameTxt.setText("");
            lnameTxt.setText("");
            phoneTxt.setText("");
            emailTxt.setText("");
            tinTxt.setText("");
            addressTxt.setText("");
            typeCombo.setSelectedItem("Choose");

            //UPDATE TABLE
            DefaultTableModel model = (DefaultTableModel) customerTable.getModel();
            model.setRowCount(0);
            showCustomers();
    }
    

